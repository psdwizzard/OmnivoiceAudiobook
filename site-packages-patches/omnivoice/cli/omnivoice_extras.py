def _resolve_voices_dir() -> Path:
    env = os.environ.get("OMNIVOICE_VOICES_DIR")
    if env:
        return Path(env).expanduser().resolve()
    return (Path.cwd() / "voices").resolve()


VOICES_DIR: Path = _resolve_voices_dir()


def _resolve_generations_dir() -> Path:
    env = os.environ.get("OMNIVOICE_GENERATIONS_DIR")
    if env:
        return Path(env).expanduser().resolve()
    return (Path.cwd() / "generations").resolve()


GENERATIONS_DIR: Path = _resolve_generations_dir()

# ---------------------------------------------------------------------------
# Per-user generation isolation (Forge / Cloudflare Access).
#
# The generation library is split per user when WorldEngine/OmniVoice runs
# behind Cloudflare Access: each request carries the user's identity, and the
# demo handlers call set_generations_dir_for_request() at entry to point all
# generation reads/writes at GENERATIONS_DIR/<user>. Voices stay shared
# (VOICES_DIR is intentionally NOT overridden).
#
# A ContextVar gives correct isolation under Gradio's threaded/queued event
# loop: each event runs in its own copied context, so concurrent users never
# see each other's override. When no override is set (e.g. direct LAN access
# with no CF header) the shared root GENERATIONS_DIR is used.
# ---------------------------------------------------------------------------

import contextvars as _contextvars

_GEN_DIR_OVERRIDE: "_contextvars.ContextVar[Optional[Path]]" = _contextvars.ContextVar(
    "omnivoice_gen_dir_override", default=None
)


def set_generations_dir_for_request(user_key: Optional[str]) -> None:
    """Scope the generation library to a per-user subfolder for this request.

    Pass a falsy user_key to clear the override (use the shared root).
    """
    if not user_key:
        _GEN_DIR_OVERRIDE.set(None)
        return
    safe = slugify(user_key)
    _GEN_DIR_OVERRIDE.set((GENERATIONS_DIR / safe).resolve())


def _gen_root() -> Path:
    """Current generation root: per-user override if set, else shared root."""
    return _GEN_DIR_OVERRIDE.get() or GENERATIONS_DIR

_SLUG_RE = re.compile(r"[^a-z0-9]+")


def slugify(name: str) -> str:
    s = (name or "").strip().lower()
    s = _SLUG_RE.sub("-", s).strip("-")
    return s or "voice"


def _voice_dir(slug: str) -> Path:
    return VOICES_DIR / slug


def list_saved_voices() -> List[Dict[str, Any]]:
    if not VOICES_DIR.exists():
        return []
    out: List[Dict[str, Any]] = []
    for sub in sorted(VOICES_DIR.iterdir()):
        meta_path = sub / "voice.json"
        if not meta_path.is_file():
            continue
        try:
            meta = json.loads(meta_path.read_text(encoding="utf-8"))
            meta.setdefault("slug", sub.name)
            out.append(meta)
        except Exception as e:
            logger.warning("Skipping invalid voice %s: %s", sub.name, e)
    return out


def voice_choices() -> List[Tuple[str, str]]:
    """Return Gradio dropdown choices: [(display_label, slug), ...] with a None entry."""
    items = [("(none)", "")]
    for m in list_saved_voices():
        label = m.get("name") or m.get("slug")
        items.append((label, m["slug"]))
    return items


def save_voice(
    model,
    *,
    name: str,
    description: str,
    ref_audio_path: str,
    ref_text: Optional[str],
    instruct: Optional[str],
    language: Optional[str],
    defaults: Dict[str, Any],
    overwrite: bool = False,
) -> str:
    if not (name or "").strip():
        raise ValueError("Voice name is required.")
    if not ref_audio_path:
        raise ValueError("Reference audio is required.")

    slug = slugify(name)
    target = _voice_dir(slug)
    if target.exists() and not overwrite:
        raise FileExistsError(
            f"A voice named '{slug}' already exists. "
            f"Tick 'Overwrite if exists' to replace it."
        )
    target.mkdir(parents=True, exist_ok=True)

    prompt = model.create_voice_clone_prompt(
        ref_audio=ref_audio_path,
        ref_text=(ref_text or None),
    )

    wav = load_audio(ref_audio_path, model.sampling_rate)
    if wav.ndim == 2:
        wav_out = wav.T
    else:
        wav_out = wav
    sf.write(
        str(target / "reference.wav"),
        wav_out,
        int(model.sampling_rate),
        subtype="PCM_16",
    )

    torch.save(
        {
            "ref_audio_tokens": prompt.ref_audio_tokens.detach().cpu(),
            "ref_text": prompt.ref_text,
            "ref_rms": float(prompt.ref_rms),
        },
        str(target / "voice.pt"),
    )

    meta = {
        "name": name.strip(),
        "slug": slug,
        "description": (description or "").strip(),
        "language": language or "Auto",
        "instruct": ((instruct or "").strip() or None),
        "defaults": dict(defaults or {}),
        "created_at": _dt.datetime.now().isoformat(timespec="seconds"),
    }
    (target / "voice.json").write_text(
        json.dumps(meta, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    logger.info("Saved voice '%s' to %s", slug, target)
    return slug


def load_voice_prompt(model, slug: str) -> Tuple[VoiceClonePrompt, Dict[str, Any]]:
    target = _voice_dir(slug)
    if not target.is_dir():
        raise FileNotFoundError(
            f"No saved voice named '{slug}' under {VOICES_DIR}."
        )

    payload = torch.load(str(target / "voice.pt"), map_location="cpu")
    tokenizer_device = model.audio_tokenizer.device
    prompt = VoiceClonePrompt(
        ref_audio_tokens=payload["ref_audio_tokens"].to(tokenizer_device),
        ref_text=payload["ref_text"],
        ref_rms=float(payload["ref_rms"]),
    )
    meta = json.loads((target / "voice.json").read_text(encoding="utf-8"))
    meta.setdefault("slug", slug)
    return prompt, meta


def delete_voice(slug: str) -> None:
    if not slug:
        return
    target = _voice_dir(slug)
    if target.is_dir():
        shutil.rmtree(target)
        logger.info("Deleted voice '%s'", slug)


# ---------------------------------------------------------------------------
# Paragraph-aware text generation
# ---------------------------------------------------------------------------

def split_text_by_newlines(text: str) -> List[str]:
    """Split on single '\\n'; drop empty/whitespace-only segments."""
    if not text:
        return []
    return [seg.strip() for seg in text.split("\n") if seg and seg.strip()]


def make_silence(seconds: float, sampling_rate: int) -> np.ndarray:
    n = max(0, int(round(float(seconds) * int(sampling_rate))))
    return np.zeros(n, dtype=np.float32)


def generate_with_paragraph_pauses(
    model,
    *,
    text: str,
    pause_seconds: float = 0.1,
    **gen_kwargs,
) -> np.ndarray:
    """Split text by single newlines, generate each segment, concat with silence."""
    segments = split_text_by_newlines(text)
    if not segments:
        raise ValueError("No non-empty text segments after splitting.")

    if len(segments) == 1:
        audios = model.generate(text=segments[0], **gen_kwargs)
    else:
        audios = model.generate(text=segments, **gen_kwargs)

    silence = make_silence(pause_seconds, model.sampling_rate)
    parts: List[np.ndarray] = []
    for i, audio in enumerate(audios):
        arr = np.asarray(audio, dtype=np.float32)
        if i > 0 and silence.size > 0:
            parts.append(silence)
        parts.append(arr)
    return np.concatenate(parts) if parts else np.zeros(0, dtype=np.float32)


# ---------------------------------------------------------------------------
# Recoverable long-form generation library
# ---------------------------------------------------------------------------

_SENTENCE_RE = re.compile(r"(?<=[.!?。！？])\s+")


def split_text_for_long_form(text: str, max_chars: int = 900) -> List[str]:
    """Split long text into recoverable generation chunks."""
    chunks: List[str] = []
    for para in split_text_by_newlines(text):
        if len(para) <= max_chars:
            chunks.append(para)
            continue

        current = ""
        for sentence in _SENTENCE_RE.split(para):
            sentence = sentence.strip()
            if not sentence:
                continue
            if current and len(current) + 1 + len(sentence) > max_chars:
                chunks.append(current)
                current = sentence
            elif current:
                current = f"{current} {sentence}"
            else:
                current = sentence
        if current:
            chunks.append(current)

    return chunks


def _generation_dir(slug: str) -> Path:
    return _gen_root() / slug


def _generation_meta_path(slug: str) -> Path:
    return _generation_dir(slug) / "generation.json"


def _now_iso() -> str:
    return _dt.datetime.now().isoformat(timespec="seconds")


def _read_generation_meta(path: Path) -> Dict[str, Any]:
    meta = json.loads(path.read_text(encoding="utf-8"))
    meta.setdefault("slug", path.parent.name)
    return meta


def _write_generation_meta(run_dir: Path, meta: Dict[str, Any]) -> None:
    meta["updated_at"] = _now_iso()
    (run_dir / "generation.json").write_text(
        json.dumps(meta, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )


def _chunk_path(run_dir: Path, index: int) -> Path:
    return run_dir / "chunks" / f"{index + 1:04d}.wav"


def list_generation_runs() -> List[Dict[str, Any]]:
    gen_root = _gen_root()
    if not gen_root.exists():
        return []
    out: List[Dict[str, Any]] = []
    for sub in sorted(gen_root.iterdir(), reverse=True):
        meta_path = sub / "generation.json"
        if not meta_path.is_file():
            continue
        try:
            out.append(_read_generation_meta(meta_path))
        except Exception as e:
            logger.warning("Skipping invalid generation %s: %s", sub.name, e)
    return out


def generation_choices() -> List[Tuple[str, str]]:
    items = [("(new generation)", "")]
    for meta in list_generation_runs():
        title = meta.get("title") or meta.get("slug")
        status = meta.get("status", "unknown")
        done = int(meta.get("completed_chunks") or 0)
        total = int(meta.get("chunk_count") or 0)
        items.append((f"{title} [{status} {done}/{total}]", meta["slug"]))
    return items


def format_generation_library() -> str:
    runs = list_generation_runs()
    if not runs:
        return "_No generations saved yet._"
    lines = [
        "| Title | Slug | Status | Chunks | Updated | Output |",
        "|---|---|---|---:|---|---|",
    ]
    for meta in runs:
        title = (meta.get("title") or "").replace("|", "\\|")
        slug = meta.get("slug", "")
        status = meta.get("status", "")
        chunks = f"{int(meta.get('completed_chunks') or 0)}/{int(meta.get('chunk_count') or 0)}"
        updated = (meta.get("updated_at") or "").replace("T", " ")
        output = meta.get("final_mp3") or meta.get("partial_wav") or ""
        output_name = Path(output).name if output else ""
        lines.append(f"| {title} | `{slug}` | {status} | {chunks} | {updated} | {output_name} |")
    return "\n".join(lines)


def get_generation_output_path(slug: str) -> Optional[str]:
    """Return a downloadable audio file for a saved run, or None.

    Resolves against the current (per-user) run directory rather than the
    absolute path stored in generation.json, so it stays correct even for runs
    that were moved between library roots (e.g. migrated into a per-user
    folder). Prefers the final MP3, then final WAV, then the partial WAV.
    """
    if not slug:
        return None
    run_dir = _generation_dir(slug)
    for name in ("final.mp3", "final.wav", "partial.wav"):
        candidate = run_dir / name
        if candidate.is_file():
            return str(candidate)
    return None


def load_generation_text(slug: str) -> Tuple[str, str]:
    if not slug:
        return "", ""
    run_dir = _generation_dir(slug)
    meta_path = run_dir / "generation.json"
    text_path = run_dir / "source.txt"
    if not meta_path.is_file():
        return "", ""
    meta = _read_generation_meta(meta_path)
    text = text_path.read_text(encoding="utf-8") if text_path.is_file() else ""
    return meta.get("title") or "", text


def load_generation_meta(slug: str) -> Dict[str, Any]:
    if not slug:
        return {}
    meta_path = _generation_meta_path(slug)
    if not meta_path.is_file():
        return {}
    return _read_generation_meta(meta_path)


def _new_generation_slug(title: str) -> str:
    stamp = _dt.datetime.now().strftime("%Y%m%d-%H%M%S")
    return f"{stamp}-{slugify(title or 'generation')}"


def _combined_audio_from_chunks(
    run_dir: Path,
    chunk_count: int,
    pause_seconds: float,
    sampling_rate: int,
) -> np.ndarray:
    silence = make_silence(pause_seconds, sampling_rate)
    parts: List[np.ndarray] = []
    for i in range(chunk_count):
        path = _chunk_path(run_dir, i)
        if not path.is_file():
            break
        audio, sr = sf.read(str(path), dtype="float32", always_2d=False)
        if sr != sampling_rate:
            raise ValueError(f"Chunk {path.name} has sample rate {sr}, expected {sampling_rate}.")
        arr = np.asarray(audio, dtype=np.float32)
        if arr.ndim > 1:
            arr = arr.mean(axis=1)
        if parts and silence.size > 0:
            parts.append(silence)
        parts.append(arr)
    return np.concatenate(parts) if parts else np.zeros(0, dtype=np.float32)


def _write_combined_outputs(
    run_dir: Path,
    meta: Dict[str, Any],
    *,
    final: bool,
) -> Optional[str]:
    audio = _combined_audio_from_chunks(
        run_dir,
        int(meta.get("chunk_count") or 0),
        float(meta.get("pause_seconds") or 0.0),
        int(meta["sampling_rate"]),
    )
    if audio.size == 0:
        return None

    wav_name = "final.wav" if final else "partial.wav"
    wav_path = run_dir / wav_name
    sf.write(str(wav_path), audio, int(meta["sampling_rate"]), subtype="PCM_16")
    meta["final_wav" if final else "partial_wav"] = str(wav_path)

    if final:
        mp3_path = run_dir / "final.mp3"
        sf.write(
            str(mp3_path),
            audio,
            int(meta["sampling_rate"]),
            format="MP3",
            subtype="MPEG_LAYER_III",
        )
        meta["final_mp3"] = str(mp3_path)
        return str(mp3_path)
    return str(wav_path)


def _prepare_generation_run(
    *,
    title: str,
    text: str,
    resume_slug: Optional[str],
    sampling_rate: int,
    pause_seconds: float,
    settings: Dict[str, Any],
    max_chars: int,
) -> Tuple[Path, Dict[str, Any], List[str]]:
    if resume_slug:
        run_dir = _generation_dir(resume_slug)
        meta = _read_generation_meta(run_dir / "generation.json")
        saved_text = (run_dir / "source.txt").read_text(encoding="utf-8")
        chunks = split_text_for_long_form(saved_text, int(meta.get("max_chars") or max_chars))
        return run_dir, meta, chunks

    clean_title = (title or "").strip() or "Untitled Generation"
    clean_text = (text or "").strip()
    chunks = split_text_for_long_form(clean_text, max_chars)
    if not chunks:
        raise ValueError("No non-empty text chunks after splitting.")

    slug = _new_generation_slug(clean_title)
    run_dir = _generation_dir(slug)
    run_dir.mkdir(parents=True, exist_ok=False)
    (run_dir / "chunks").mkdir(parents=True, exist_ok=True)
    (run_dir / "source.txt").write_text(clean_text, encoding="utf-8")
    meta = {
        "title": clean_title,
        "slug": slug,
        "status": "pending",
        "created_at": _now_iso(),
        "updated_at": _now_iso(),
        "chunk_count": len(chunks),
        "completed_chunks": 0,
        "sampling_rate": int(sampling_rate),
        "pause_seconds": float(pause_seconds or 0.0),
        "max_chars": int(max_chars),
        "settings": settings,
        "final_wav": None,
        "final_mp3": None,
        "partial_wav": None,
        "error": None,
    }
    _write_generation_meta(run_dir, meta)
    return run_dir, meta, chunks


def generate_long_form_run(
    model,
    *,
    title: str,
    text: str,
    resume_slug: Optional[str],
    pause_seconds: float,
    settings: Dict[str, Any],
    gen_kwargs: Dict[str, Any],
    max_chars: int = 900,
) -> Iterator[Dict[str, Any]]:
    """Generate a recoverable long-form run and yield progress dictionaries."""
    run_dir, meta, chunks = _prepare_generation_run(
        title=title,
        text=text,
        resume_slug=resume_slug,
        sampling_rate=int(model.sampling_rate),
        pause_seconds=float(pause_seconds or 0.0),
        settings=settings,
        max_chars=int(max_chars or 900),
    )
    total = len(chunks)
    start = time.time()
    meta["status"] = "running"
    meta["chunk_count"] = total
    meta["error"] = None
    _write_generation_meta(run_dir, meta)

    try:
        for i, chunk in enumerate(chunks):
            chunk_path = _chunk_path(run_dir, i)
            if chunk_path.is_file():
                meta["completed_chunks"] = max(int(meta.get("completed_chunks") or 0), i + 1)
                status = "skipped"
            else:
                audios = model.generate(text=chunk, **gen_kwargs)
                audio = np.asarray(audios[0], dtype=np.float32)
                sf.write(str(chunk_path), audio, int(model.sampling_rate), subtype="PCM_16")
                meta["completed_chunks"] = i + 1
                status = "generated"

            partial = _write_combined_outputs(run_dir, meta, final=False)
            elapsed = int(time.time() - start)
            _write_generation_meta(run_dir, meta)
            yield {
                "slug": meta["slug"],
                "title": meta["title"],
                "status": "running",
                "chunk_index": i + 1,
                "chunk_count": total,
                "chunk_status": status,
                "elapsed_seconds": elapsed,
                "audio_path": partial,
                "message": (
                    f"{status.title()} chunk {i + 1}/{total}. "
                    f"Elapsed {elapsed // 60}m {elapsed % 60}s. "
                    f"Saved in {run_dir}."
                ),
            }

        final_audio = _write_combined_outputs(run_dir, meta, final=True)
        meta["status"] = "complete"
        meta["completed_chunks"] = total
        _write_generation_meta(run_dir, meta)
        yield {
            "slug": meta["slug"],
            "title": meta["title"],
            "status": "complete",
            "chunk_index": total,
            "chunk_count": total,
            "elapsed_seconds": int(time.time() - start),
            "audio_path": final_audio,
            "message": f"Done. Final MP3 saved to {final_audio}.",
        }
    except Exception as e:
        meta["status"] = "failed"
        meta["error"] = f"{type(e).__name__}: {e}"
        _write_generation_meta(run_dir, meta)
        raise
