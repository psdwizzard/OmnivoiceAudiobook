def build_demo(
    model: OmniVoice,
    checkpoint: str,
    generate_fn=None,
) -> gr.Blocks:

    sampling_rate = model.sampling_rate

    # -- shared generation core --
    def _gen_core(
        text,
        language,
        ref_audio,
        instruct,
        num_step,
        guidance_scale,
        denoise,
        speed,
        duration,
        preprocess_prompt,
        postprocess_output,
        pause_seconds,
        mode,
        ref_text=None,
        saved_voice_slug=None,
    ):
        if not text or not text.strip():
            return None, "Please enter the text to synthesize."

        gen_config = OmniVoiceGenerationConfig(
            num_step=int(num_step or 32),
            guidance_scale=float(guidance_scale) if guidance_scale is not None else 2.0,
            denoise=bool(denoise) if denoise is not None else True,
            preprocess_prompt=bool(preprocess_prompt),
            postprocess_output=bool(postprocess_output),
        )

        lang = language if (language and language != "Auto") else None

        kw: Dict[str, Any] = dict(language=lang, generation_config=gen_config)

        if speed is not None and float(speed) != 1.0:
            kw["speed"] = float(speed)
        if duration is not None and float(duration) > 0:
            kw["duration"] = float(duration)

        if mode == "clone":
            if saved_voice_slug:
                try:
                    prompt, meta = ovx.load_voice_prompt(model, saved_voice_slug)
                except Exception as e:
                    return None, f"Could not load saved voice: {type(e).__name__}: {e}"
                kw["voice_clone_prompt"] = prompt
                # If the saved voice has an instruct and the user didn't supply one,
                # fall back to the saved instruct.
                if (not instruct or not instruct.strip()) and meta.get("instruct"):
                    instruct = meta["instruct"]
            elif ref_audio:
                kw["voice_clone_prompt"] = model.create_voice_clone_prompt(
                    ref_audio=ref_audio,
                    ref_text=ref_text,
                )
            else:
                return None, "Please upload a reference audio or pick a saved voice."

        if instruct and instruct.strip():
            kw["instruct"] = instruct.strip()

        try:
            waveform_f = ovx.generate_with_paragraph_pauses(
                model,
                text=text.strip(),
                pause_seconds=float(pause_seconds or 0.0),
                **kw,
            )
        except Exception as e:
            return None, f"Error: {type(e).__name__}: {e}"

        waveform = (waveform_f * 32767).astype(np.int16)
        return (sampling_rate, waveform), "Done."

    # Allow external wrappers (e.g. spaces.GPU for ZeroGPU Spaces)
    _gen = generate_fn if generate_fn is not None else _gen_core

    # =====================================================================
    # UI
    # =====================================================================

    # Reusable: language dropdown component
    def _lang_dropdown(label="Language (optional) / 语种 (可选)", value="Auto"):
        return gr.Dropdown(
            label=label,
            choices=_ALL_LANGUAGES,
            value=value,
            allow_custom_value=False,
            interactive=True,
            info="Keep as Auto to auto-detect the language.",
        )

